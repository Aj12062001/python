f=float(input("enter your first no.:"))
s=float(input("enter your second no.:"))
add=f+s
sub=f-s
div=f/s
multi=f*s
mod=f%s
print(f"addition:{f}+{s}={add:.2f}")
print(f"substration:{f}-{s}={sub:.2f}")
print(f"division:{f}/{s}={div:.2f}")
print(f"multiplication:{f}*{s}={multi:.2f}")
print(f"modulus:{f}%{s}={mod:.2f}")


